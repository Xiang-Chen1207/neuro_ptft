import os
from PIL import Image

def combine_images():
    base_dir = '/vepfs-0x0d/home/cx/ptft/experiments/tuab_lp/plots_separation'
    output_path = os.path.join(base_dir, 'combined_separation.png')
    
    # Files in order
    files = [
        'separation_Baseline_Recon.png',
        'separation_Neuro-KE.png',
        'separation_FeatOnly.png'
    ]
    
    images = []
    for f in files:
        path = os.path.join(base_dir, f)
        if os.path.exists(path):
            images.append(Image.open(path))
        else:
            print(f"Warning: {path} not found")
            return

    if not images:
        print("No images found to combine.")
        return

    # Assume all images are same size since they were generated by the same script
    width, height = images[0].size
    
    # Create combined image (Horizontal)
    combined_width = width * len(images)
    combined_height = height
    
    combined_image = Image.new('RGB', (combined_width, combined_height), 'white')
    
    for i, img in enumerate(images):
        combined_image.paste(img, (i * width, 0))
        
    print(f"Saving combined image to {output_path}...")
    combined_image.save(output_path)
    print("Done.")

if __name__ == "__main__":
    combine_images()
